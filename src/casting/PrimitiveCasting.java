package casting;

public class PrimitiveCasting {

	public static void main(String[] args) {
		// changing one type of information into other type
		
		// Implicit casting- converting lower data type into higher data type
		
		int a=10;
		double b= a;
		System.out.println(b);
		// Explicit casting- converting higher data type into lower data type
		
		float f=10.23f;
		int i=(int)f;
		System.out.println(i);
		
		


	}

}
